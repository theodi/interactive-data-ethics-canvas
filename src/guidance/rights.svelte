<p>
  Once you have established your data sources it is important to ensure that you have the rights to reuse the data for your intended purpose. It is also important to understand what obligations you must fulfil as a result of having access to data, especially commercial or sensitive data.
</p>

<p>
  Use this space to specify the license or rights that permit you to collect, use or share data and describe any aspects related to personal or sensitive data that must be respected.
</p>

