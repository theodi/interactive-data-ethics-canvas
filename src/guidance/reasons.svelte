<p>
This section should outline the specific reasons for collecting, using and sharing the data as detailed in your data sources. Be careful to not just justify all activities with the same value proposition to that of the business or organisation. For example the data could play a vital role in providing a service. Another potential is to think about what would be possible without the data and thus prove the negative hypothesis that collecting, using and sharing data is absolutely necessary to deliver your value proposition.
