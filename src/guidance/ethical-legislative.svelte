<p>Beyond data protection, there is likely to be other legislation, ethical frameworks or codes of practice that apply to the sector or project. For example in the health care sector there are many variants of the Hippocratic Oath which must be upheld.
</p>

<p>You might also need to consider:</p>

<ul>
  <li>the rule of law  in different countries where you will be collecting, using or sharing data;</li>
  <li>human rights and anti-discrimination laws;</li>
  <li>data protection;</li>
  <li>IP and database rights;</li>
  <li>data sharing, policies, regulation and ethics codes/frameworks specific to sectors (eg health, employment, taxation);</li>
</ul>

