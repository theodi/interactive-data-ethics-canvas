<p>Unethical impacts are often the result of a lack of openness and transparency, that are in turn a result of unaddressed fears and hoarding tendencies related to data.</p>

<p>The <a href="https://theodi.org/about-the-odi/our-vision-and-manifesto/our-theory-of-change/" style="color: inherit" target="_blank">ODI theory of change</a> looks at how overcoming these fears and hoarding tendencies is essential to get the most from data.</p>

