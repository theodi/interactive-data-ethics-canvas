<p>
  Before any data can be collected, used or shared, it is important to
  understand who owns what data, who has rights over it and what permissions or
  responsibilities you or others have in relation to the data. Projects that set
  out with good intentions to reuse existing data often encounter legal and
  ethical constraints which should be addressed as soon as possible.
</p>

