<p>
There are many steps that can be taken to minimise negative impacts, from collecting a wider variety of data, to increasing transparency of processes and broadening engagement with stakeholders.
</p>
<p>
One key thing to avoid is behavior that will in fact increase unethical impacts, such as collecting more data that will simply emphasise any existing biases. 
</p>
<p>
There are also many steps that can be taken to minimise risk, such as carefully considering what data needs to be collected and retained and perhaps using anonymisation techniques to remove the risks from handling personal data.
</p>

