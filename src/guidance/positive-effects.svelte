<p>
When thinking about the effects, consideration should be paid to societal, environmental and economic impacts. Consideration should also be given to those outside of your immediate ecosystem. For example, if you are designing a digital service, what about those who are not as comfortable in a digital environment? Are all demographics considered?
</p>

<p>Consider also:</p>
        
<ul>
  <li>people whom the data is about;</li>
  <li>people impacted by its use;</li>
  <li>organisations using the data.</li>
</ul>

